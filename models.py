from sqlalchemy import Column, Integer, Float, String, DateTime
from sqlalchemy.sql import func
from database import Base

class PredictionHistory(Base):

    __tablename__ = "prediction_history"

    id = Column(Integer, primary_key=True, autoincrement=True)
    # unique id for each prediction

    housing_median_age = Column(Float)
    # median age of houses in the area

    total_rooms = Column(Float)
    # total number of rooms

    total_bedrooms = Column(Float)
    # total number of bedrooms

    population = Column(Float)
    # population of the area

    households = Column(Float)
    # number of households

    median_income = Column(Float)
    # median income of residents

    ocean_proximity = Column(String)
    # distance from ocean : NEAR BAY, INLAND, NEAR OCEAN, <1H OCEAN, ISLAND

    predicted_price = Column(Float)
    # predicted house price generated by the ML model

    timestamp = Column(DateTime, server_default=func.now())
    # automatically stores when prediction was made